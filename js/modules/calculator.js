// ADD FUNCTION
const add = (num1, num2) => {
    return num1 + num2;
};

// SUBTRACT FUNCTION
const sub = (num1, num2) => {
    return num1 - num2;
  
};

// MULTIPLY FUNCTION
const mul = (num1, num2) => {
    return num1 * num2;
};

// DIVIDE FUNCTION
const div = (num1, num2) => {
    return num1 / num2;
};

// EXPORT THE FOUR PUBLIC FUNCTIONS
export {add};
export {sub};
export {mul};
export {div}; 